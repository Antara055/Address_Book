package com.bridgelab;

import java.util.Scanner;

public class Contact_info {
    String first_name;
    String last_name;
    String email;
    long phone_no;
    String city;
    String state;
    int zip;

    public Contact_info(String first_name, String last_name, String email, long phone_no, String city, String state, int zip) {
        this.first_name = first_name;
        this.last_name = last_name;
        this.email = email;
        this.phone_no = phone_no;
        this.city = city;
        this.state = state;
        this.zip = zip;

    }


}

